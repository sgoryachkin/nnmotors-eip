package ru.nnmotors.eip.web.ui.component.filter;

public class SimpleTextFilter {
	
	private String text;
	
	public SimpleTextFilter() {
		super();
	}

	public SimpleTextFilter(String text) {
		super();
		this.text = text;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

}
